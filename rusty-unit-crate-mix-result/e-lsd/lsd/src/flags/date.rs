mod tests_rug_87 {
    use super::*;
    use crate::flags::{Configurable, date::DateFlag};

    #[test]
    fn test_rug() {
        let _full_iso_value: String = "full-iso".into();
        let _long_iso_value: String = "long-iso".into();
        let _iso_value: String = "iso".into();
        let _custom_format_value: String = "+%Y-%m-%d".into();
        let _invalid_value: String = "invalid".into();

        std::env::set_var("TIME_STYLE", &_full_iso_value);
        assert_eq!(<DateFlag as Configurable<DateFlag>>::from_environment(), Some(DateFlag::Formatted("%F %T.%f %z".into())));

        std::env::set_var("TIME_STYLE", &_long_iso_value);
        assert_eq!(<DateFlag as Configurable<DateFlag>>::from_environment(), Some(DateFlag::Formatted("%F %R".into())));

        std::env::set_var("TIME_STYLE", &_iso_value);
        assert_eq!(<DateFlag as Configurable<DateFlag>>::from_environment(), Some(DateFlag::Iso));

        std::env::set_var("TIME_STYLE", &_custom_format_value);
        assert_eq!(<DateFlag as Configurable<DateFlag>>::from_environment(), Some(DateFlag::Formatted(_custom_format_value.into())));

        std::env::set_var("TIME_STYLE", &_invalid_value);
        assert_eq!(<DateFlag as Configurable<DateFlag>>::from_environment(), None);

        std::env::remove_var("TIME_STYLE");
        assert_eq!(<DateFlag as Configurable<DateFlag>>::from_environment(), None);
    }
}#[cfg(test)]
